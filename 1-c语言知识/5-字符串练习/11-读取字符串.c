/*2、输入一个字符串ch，并做以下更改后输出： 
1：如果里面有a或者A ，都输出*；
2：如果里面有b或者B ，都输出#；
其它的数据保持不变。如果输入的是字符串第一个字符ch[0]是x， 则退出程序*/

#include <stdio.h>

int main() {
    char ch[100]; // 假设字符串长度不超过100个字符

    // 读取字符串
    printf("请输入一个字符串: ");
    scanf("%99s", ch); // 限制输入长度为99个字符，以避免溢出

    // 检查第一个字符是否为 'x'，如果是则退出程序
    if (ch[0] == 'x') {
        printf("程序已退出。\n");
        return 0;
    }

    // 遍历字符串，并根据规则进行替换
    for (int i = 0; ch[i] != '\0'; i++) {
        if (ch[i] == 'a' || ch[i] == 'A') {
            ch[i] = '*';
        } else if (ch[i] == 'b' || ch[i] == 'B') {
            ch[i] = '#';
        }
    }

    // 输出替换后的字符串
    printf("更改后的字符串: %s\n", ch);

    return 0;
}
