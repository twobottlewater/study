/*3编写一个程序，清除用户输入字符串中的空格符并将之输出。（例如用户输入”a b”，输出”ab”）*/

#include <stdio.h>

int main() {
    char ch[100]; // 假设字符串长度不超过100个字符

    // 读取字符串
    printf("请输入一个字符串: ");
    scanf("%s", ch); // 限制输入长度为99个字符，以避免溢出

    // 检查第一个字符是否为 'x'，如果是则退出程序
    if (ch[0] == 'x') {
        printf("程序已退出。\n");
        return 0;
    }

    // 遍历字符串，并根据规则进行替换
    for (int i = 0; ch[i] != '\0'; i++) {
        if (ch[i] == 'a' || ch[i] == 'A') {
            ch[i] = '*';
        } else if (ch[i] == 'b' || ch[i] == 'B') {
            ch[i] = '#';
        }
    }

    // 输出替换后的字符串
    printf("更改后的字符串: %s\n", ch);

    return 0;
}
